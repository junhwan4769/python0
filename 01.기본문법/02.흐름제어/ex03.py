for i in range(10): #0부터 9까지
    print(i, end= ',')

print('\n', '-' * 20)

for i in range(1, 11): #1부터 10까지
    print(i, end= ',')

print('\n', '-' * 20)

for i in range(2, 11, 2): #2부터 10까지 짝수
    print(i, end= ',')

print('\n', '-' * 20)

for i in range(1, 10, 2): #1부터 9까지 홀수
    print(i, end= ',')